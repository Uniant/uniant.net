                    <span>第 3 条　個人情報の収集方法</span>
                </div>
                <div class="articlemain">
                    <span>当グループは、ユーザーが利用登録をする際に、氏名、生年月日、メールアドレスなどの個人情報をお尋ねすることがあります。また、ユーザーと提携先との間でなされたユーザーの個人情報に関する情報を、当グループの提携先（情報提供元、広告主などを含みます。）などから収集することがあります。</span>
