                    <span>第 5 条　利用目的の変更</span>
                </div>
                <div class="articlemain">
                    <span>当グループは、利用目的が変更前と関連性を有すると合理的に認められる場合に限り、個人情報の利用目的を変更するものとします。利用目的の変更を行った場合には、当グループウェブサイトに公表するものとします。</span><br>
