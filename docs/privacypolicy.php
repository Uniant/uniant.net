                        <span>・<a href="https://elaws.e-gov.go.jp/search/elawsSearch/elaws_search/lsg0500/detail?lawId=415AC0000000057#123" target="_blank" class="animebuttonlite">個人情報保護法第 23 条第 1 項</a>の内容に準ずる場合。</span><br>
